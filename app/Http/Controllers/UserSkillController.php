    {
        //

        $id=Auth::user()->id;


        $userskill=UserSkill::find($id);

        if($userskill == null){
            UserSkill::create($request->all());
        }
        else{
            UserSkill::where('user_id',$id)->update(['skill'=>$request->skill]);
        }

    }

